- (void)viewDidLoad
{
    [super viewDidLoad];

    CGFloat height;
    [super viewDidLoad];
    
    height = [_detailChoices count] * 44;
    self.clearsSelectionOnViewWillAppear = YES;
    self.contentSizeForViewInPopover = CGSizeMake(150.0, height);
    self.clearsSelectionOnViewWillAppear = YES;
 }

